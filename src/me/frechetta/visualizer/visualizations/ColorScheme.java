package me.frechetta.visualizer.visualizations;

public class ColorScheme
{
	private float r, g, b, a;
	private float r2, g2, b2, a2;
	
	
	
	public ColorScheme(float _r, float _g, float _b, float _a, float _r2, float _g2, float _b2, float _a2)
	{
		r = _r;
		g = _g;
		b = _b;
		a = _a;
		r2 = _r2;
		g2 = _g2;
		b2 = _b2;
		a2 = _a2;
	}


	public float getR()
	{
		return r;
	}


	public void setR(float r)
	{
		this.r = r;
	}


	public float getG()
	{
		return g;
	}


	public void setG(float g)
	{
		this.g = g;
	}


	public float getB()
	{
		return b;
	}


	public void setB(float b)
	{
		this.b = b;
	}


	public float getA()
	{
		return a;
	}


	public void setA(float a)
	{
		this.a = a;
	}


	public float getR2()
	{
		return r2;
	}


	public void setR2(float r2)
	{
		this.r2 = r2;
	}


	public float getG2()
	{
		return g2;
	}


	public void setG2(float g2)
	{
		this.g2 = g2;
	}


	public float getB2()
	{
		return b2;
	}


	public void setB2(float b2)
	{
		this.b2 = b2;
	}


	public float getA2()
	{
		return a2;
	}


	public void setA2(float a2)
	{
		this.a2 = a2;
	}
	
	
	
}
